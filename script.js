// Start spinning with smooth infinite animation
const startSpin = () => {
  if (isSpinning) return;
  isSpinning = true;
  winnerSection.classList.add("hidden");
  startButton.disabled = true;
  stopButton.disabled = false;
  let offset = 0;
  const slotHeight = 350; // Adjust to match the height of each image item

  selectedList = selectAndRemoveRandomItems(SELECTRANDOM);
  console.log("selectedList: ", selectedList);
  selectedEmployees = selectedList.map((key) => data[key]);
  console.log("selectedEmployees: ", selectedEmployees);

  populateImages(selectedEmployees);
  populateSlot();
  spinningInterval = setInterval(() => {
    offset = (offset + spinSpeed) % (slotHeight * images.length);
    slotBox.style.transition = "none";
    slotBox.style.transform = `translateY(-${offset}px)`;
  }, 16); // Smooth 60 FPS animation
};

// Stop spinning and add slowing effect
const stopSpin = () => {
  if (!isSpinning) return;
  isSpinning = false;
  clearInterval(spinningInterval);

  let offset =
    parseFloat(
      slotBox.style.transform.replace("translateY(-", "").replace("px)", "")
    ) || 0;
  const slotHeight = 300;
  let velocity = spinSpeed * 3; // Initial velocity for slowing down

  const slowDown = () => {
    if (velocity > 0.5) {
      velocity *= decelerationFactor; // Gradual deceleration
      offset += velocity;
      offset %= slotHeight * images.length;
      slotBox.style.transition = "none";
      slotBox.style.transform = `translateY(-${offset}px)`;
      requestAnimationFrame(slowDown);
    } else {
      // Snap to the closest slot and declare the winner
      const winnerIndex = Math.round(offset / slotHeight) % images.length;
      offset = winnerIndex * slotHeight;
      slotBox.style.transition = "transform 0.5s ease";
      slotBox.style.transform = `translateY(-${offset}px)`;

      setTimeout(() => {
        const winnerImageURL = images[winnerIndex];
        winnerImage.src = winnerImageURL;
        console.log(winnerIndex);
        console.log(selectedEmployees);
        winnerText.textContent = `Name: ${selectedEmployees[winnerIndex]["Name"]}\nID: ${selectedEmployees[winnerIndex]["Emp Id"]}`;
        winnerSection.classList.remove("hidden");
        launchConfetti();
        startButton.disabled = false;
        stopButton.disabled = true;

        // Store winner in localStorage
        const winnerID = selectedList[winnerIndex]; // Assuming each employee object contains an `id`
        console.log("winnerId: ", winnerID);
        const storedWinners = JSON.parse(localStorage.getItem("winners")) || [];
        storedWinners.push(winnerID); // Add new winner ID to the list
        localStorage.setItem("winners", JSON.stringify(storedWinners)); // Save updated list

        console.log("Winner stored in localStorage:", storedWinners);
      }, 500);
    }
  };

  slowDown();
};

// Launch confetti
const launchConfetti = () => {
  confetti({
    particleCount: 100,
    spread: 70,
    origin: { y: 0.6 },
  });
};

// Event Listeners
startButton.addEventListener("click", startSpin);
stopButton.addEventListener("click", stopSpin);

// Initial Setup

document
  .getElementById("toggle_winner_list")
  .addEventListener("click", function () {
    const winnerListContainer = document.getElementById(
      "winner_section_container"
    );
    renderWinnerList();

    // Toggle visibility of winner list
    if (
      winnerListContainer.style.display === "none" ||
      winnerListContainer.style.display === ""
    ) {
      winnerListContainer.style.display = "block"; // Show the winner list
      winnerListContainer.style.animation = "slide-in 1s ease-out"; // Add slide-in animation
      this.textContent = "Hide Winner List"; // Change button text
    } else {
      winnerListContainer.style.animation = "none"; // Remove the slide-in animation
      winnerListContainer.style.display = "none"; // Hide the winner list
      this.textContent = "Show Winner List"; // Change button text back
    }
  });

// Function to render the winner list from localStorage
function renderWinnerList() {
  const winnerList = JSON.parse(localStorage.getItem("winners")) || []; // Get winners from localStorage
  const winnerSection = document.querySelector("#winner_section_container");
  const winnerListContainer = winnerSection.querySelector(".winning.list"); // Target the <ul> with class 'winning list'

  // Clear any existing content in the winner list
  winnerListContainer.innerHTML = "";

  // Loop through the winner list and create the list items
  winnerList.forEach((winnerId, index) => {
    console.log("winerID new: ", winnerId);
    const listItem = document.createElement("li");
    listItem.className = "winner";
    console.log("selectedEmp: ", selectedEmployees);

    listItem.innerHTML = `
            <div class='drag-details'>
                <img src='${data[winnerId]["Photo"]}' alt='Winner Image' class='drag-img'>
                <span>${data[winnerId]["Name"]} <br> (ID: ${data[winnerId]["Emp Id"]})</span>
            </div>
        `;

    winnerListContainer.appendChild(listItem);
  });

  // Show the winner section if there are winners
  if (winnerList.length > 0) {
    winnerSection.classList.remove("hidden");
  } else {
    winnerSection.classList.add("hidden");
  }
}

